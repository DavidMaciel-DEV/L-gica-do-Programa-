#Média Aritimetica Simples
Valor1 = int(input("Digite o primeiro número: "))
Valor2 = int(input("Digite o segundo número: "))
valor3 = int(input("Digite o quarto número: "))
Valor4 = int(input("Digite o quarto número: "))
As = (Valor1 + Valor2 + valor3+ Valor4) / 4
print("A média aritimetica simples é", As)

#Média Aritimetica Geometrica
Ag = Valor1 * Valor2 * valor3 * Valor4 **(1/2)
print("A média aritimetica geometrica é", Ag)

#Média Aritimetica Harmonica
Ah = (1 / Valor1 + 1/Valor2 + 1/valor3 + 1/Valor4) / 4
print("A média aritimetica harmonica é", Ah)